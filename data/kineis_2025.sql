
CREATE DATABASE IF NOT EXISTS satellite_data;
USE satellite_data;

CREATE TABLE IF NOT EXISTS ephemeris (
    id INT AUTO_INCREMENT PRIMARY KEY,
    satellite_name VARCHAR(100),
    timestamp DATETIME,
    x DOUBLE,
    y DOUBLE,
    z DOUBLE
);

